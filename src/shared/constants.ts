export const getTemperateColor = (
    temperatureColors: object[],
    temperature: number
) => {
    return temperatureColors.reduce(
        (prevTemperatureColor, currTemperatureColor) => {
            return Math.abs(
                temperature - (currTemperatureColor as TemperatureColor).value
            ) <
                Math.abs(
                    temperature -
                        (prevTemperatureColor as TemperatureColor).value
                )
                ? currTemperatureColor
                : prevTemperatureColor;
        }
    );
};

